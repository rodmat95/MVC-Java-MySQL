    private void tblVehiculoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVehiculoMouseClicked
        int selectedRow = tblVehiculo.getSelectedRow();
        if (selectedRow != -1) {
            // Obtén los datos de la fila seleccionada
            String id = tblVehiculo.getValueAt(selectedRow, 0).toString();
            String placa = tblVehiculo.getValueAt(selectedRow, 1).toString();
            String marca = tblVehiculo.getValueAt(selectedRow, 2).toString();
            String modelo = tblVehiculo.getValueAt(selectedRow, 3).toString();
            String anio = tblVehiculo.getValueAt(selectedRow, 4).toString();
            String color = tblVehiculo.getValueAt(selectedRow, 5).toString();

            // Muestra los datos en los JTextField correspondientes
            tbxID.setText(id);
            tbxPlaca.setText(placa);
            tbxMarca.setText(marca);
            tbxModelo.setText(modelo);
            tbxAño.setText(anio);
            tbxColor.setText(color);
        }
    }//GEN-LAST:event_tblVehiculoMouseClicked

