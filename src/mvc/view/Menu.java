    private void llenarTabla() {
        List<Vehiculo> listaVehiculos = vehiculoController.listarVehiculos();

        DefaultTableModel model = (DefaultTableModel) tblVehiculo.getModel();
        model.setRowCount(0);

        for (Vehiculo vehiculo : listaVehiculos) {
            Object[] rowData = {
                vehiculo.getId(),
                vehiculo.getPlaca(),
                vehiculo.getMarca(),
                vehiculo.getModelo(),
                vehiculo.getAnio(),
                vehiculo.getColor()
            };
            model.addRow(rowData);
        }
    }   
    
    private void agregarNuevoVehiculo() {
        String placa = tbxPlaca.getText();
        String marca = tbxMarca.getText();
        String modelo = tbxModelo.getText();
        int anio = Integer.parseInt(tbxAño.getText());
        String color = tbxColor.getText();

        Vehiculo nuevoVehiculo = new Vehiculo();
        nuevoVehiculo.setPlaca(placa);
        nuevoVehiculo.setMarca(marca);
        nuevoVehiculo.setModelo(modelo);
        nuevoVehiculo.setAnio(anio);
        nuevoVehiculo.setColor(color);

        vehiculoController.agregarVehiculo(nuevoVehiculo);

        llenarTabla();
        JOptionPane.showMessageDialog(this, "Vehículo agregado exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
    }
    
    private void modificarVehiculo() {
        String idStr = tbxID.getText();

        int id = Integer.parseInt(idStr);
        String placa = tbxPlaca.getText();
        String marca = tbxMarca.getText();
        String modelo = tbxModelo.getText();
        int anio = Integer.parseInt(tbxAño.getText());
        String color = tbxColor.getText();

        Vehiculo vehiculoModificado = new Vehiculo();
        vehiculoModificado.setId(id);
        vehiculoModificado.setPlaca(placa);
        vehiculoModificado.setMarca(marca);
        vehiculoModificado.setModelo(modelo);
        vehiculoModificado.setAnio(anio);
        vehiculoModificado.setColor(color);

        vehiculoController.modificarVehiculo(vehiculoModificado);

        llenarTabla();
        JOptionPane.showMessageDialog(this, "Vehículo modificado exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
    }
    
    private void eliminarVehiculo() {
        String idStr = tbxID.getText();
        try {
            int id = Integer.parseInt(idStr);

            int confirmacion = JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar este vehículo?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
        
            if (confirmacion == JOptionPane.YES_OPTION) {
                vehiculoController.eliminarVehiculo(id);
                llenarTabla();
                JOptionPane.showMessageDialog(this, "Vehículo eliminado exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException e) {
            e.printStackTrace();
        }
    }
    
